  `note` text,
  PRIMARY KEY (`id`),
  UNIQUE KEY `unique_index` (`developer_id`,`skill_id`),
  KEY `skill_id` (`skill_id`),
  CONSTRAINT `developer_id` FOREIGN KEY (`developer_id`) REFERENCES `developers` (`id`) ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT `skill_id` FOREIGN KEY (`skill_id`) REFERENCES `skills` (`id`) ON DELETE CASCADE ON UPDATE CASCADE
